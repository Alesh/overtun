def test_tls_parser(client_hello_00, client_hello_01):
    record = TLSRecord.load(client_hello_00)
    assert record.message.type == TLSMessage.Type.ClientHello
    assert TLSExtension.Type.ServerName in record.message.extensions
    assert record.message.extensions[TLSExtension.Type.ServerName].hostname == "python.org"

    record = TLSRecord.load(client_hello_01)
    assert record.message.type == TLSMessage.Type.ClientHello
    assert TLSExtension.Type.ServerName in record.message.extensions
    assert record.message.extensions[TLSExtension.Type.ServerName].hostname == "mc.yandex.ru"
